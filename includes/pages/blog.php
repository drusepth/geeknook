<h2>On this sleepless Thursday night before class</h2>

<img src="people/photos/1.jpg" class="avatar" />
<p class="blog_entry">
So, I was thinking.  You know, projects will be a huge part of the site.  However, there will probably be other equally important
aspects that deserve their fair share.  So how do I account for all these different thingimajigs?  I thought about the concept of
apps or applications - like facebook.  This seems like a great approach, but I don't want it to look like Facebook.  (This default
layout already looks too much it already, in my humble opinion.)  So - I'm brainstorming how to approach the display of all these
[major] applications.  I'll deal with smaller ones later on.  One thing for certain, is that I don't want to clutter the menu bar
over on the right with oodles of links.  So, I give you this list!
</p>

<p class="blog_entry">
<u>Major GeekNook Aspects:</u>
<ol>
	<li><strong>Projects</strong>: They're the original building blocks of the site.  I want to provide a space to store, edit, and
		publish files in the hopes that productivity will spring to life.  Private forums, collaboration, and other things to help
		build teamwork and promote good attitudes while working with others will also be a big part.</li>
	<li><strong>Companies</strong>: It doesn't have to be a real company.  GeekNook Companies (or GCs) are basically... groups.  They
		bring you closer to the people who you work with on the projects you're working on.  They allow for a privacy and closeness
		and - dare I say it - friendship to build between people of similar interests.  When you've got friends doing amazing things,
		doesn't it make you either want to help out or do something amazing yourself?</li>
	<li><strong>Pastebin</strong>: A code directory.  Allows for adding of more than the usual pastebin information: name, author, date,
		prompt, notes, collaborators, (automatically generated) run-time output, and much more.  Each <em>paste</em> is archived for 
		easy searchability later, meaning no paste is worthless.  Whether someone wants to use your code later, or just check their own
		syntax against it, the full syntax-highlighted script is just a search away.</li>
	
</ol>
</p>

